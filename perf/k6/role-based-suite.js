import http from "k6/http";
import { check, sleep } from "k6";
import exec from "k6/execution";

const baseUrl = (__ENV.BASE_URL || "http://localhost:8080").replace(/\/$/, "");
const scenario = (__ENV.SCENARIO || "all").toLowerCase();
const vus = Number(__ENV.VUS || 5);
const duration = __ENV.DURATION || "30s";
const password = __ENV.DEMO_PASSWORD || "Candidate@123456";

export const options = {
  vus,
  duration,
  thresholds: {
    checks: ["rate>0.95"],
    http_req_failed: ["rate<0.01"],
    "http_req_duration{sla:gateway}": ["p(95)<1000"],
    "http_req_duration{sla:ai}": ["p(95)<5000"],
  },
};

function unwrap(response) {
  const body = response.json();
  return body && body.data ? body.data : body;
}

function request(method, path, token, body, tags) {
  const params = {
    headers: {},
    tags,
    timeout: "30s",
  };
  if (token) {
    params.headers.Authorization = `Bearer ${token}`;
  }
  if (body !== undefined && body !== null) {
    params.headers["Content-Type"] = "application/json";
  }
  return http.request(method, `${baseUrl}${path}`, body === undefined || body === null ? null : JSON.stringify(body), params);
}

function login(email, userPassword) {
  const response = request("POST", "/api/auth/login", null, { email, password: userPassword }, {
    flow: "auth-login",
    sla: "gateway",
  });
  check(response, {
    [`login ${email} returns token`]: (res) => res.status === 200 && Boolean(unwrap(res).accessToken),
  });
  return response.status === 200 ? unwrap(response).accessToken : null;
}

function registerCandidate(iteration) {
  const email = `k6-candidate-${Date.now()}-${exec.vu.idInTest}-${iteration}@devhire.local`;
  const response = request("POST", "/api/auth/register", null, {
    email,
    password,
    role: "CANDIDATE",
  }, {
    flow: "candidate-register",
    sla: "gateway",
  });
  check(response, {
    "candidate register returns token": (res) => res.status === 201 || res.status === 200,
  });
  if (response.status !== 201 && response.status !== 200) {
    return null;
  }
  return unwrap(response).accessToken;
}

function extractFirst(items) {
  if (!items) {
    return null;
  }
  const content = items.content || items.items || items.results || [];
  if (!Array.isArray(content) || content.length === 0) {
    return null;
  }
  return content[0];
}

function createReviewFixture(adminToken, employerToken, candidateToken) {
  const stamp = Date.now();
  const company = request("POST", "/api/companies", employerToken, {
    name: `DevHire k6 Review ${stamp}`,
    logoUrl: "https://cdn.devhire.local/logos/k6.png",
    website: "https://devhire.local",
    size: "51-200",
    industry: "Software",
    description: "Company generated for role-based k6 smoke review flow.",
  }, {
    flow: "fixture-company",
    sla: "gateway",
  });
  if (company.status !== 200) {
    return {};
  }
  const companyId = unwrap(company).id;
  request("PATCH", `/api/admin/companies/${companyId}/approve`, adminToken, null, {
    flow: "fixture-company-approve",
    sla: "gateway",
  });

  const job = request("POST", "/api/jobs", employerToken, {
    companyId,
    title: `Senior Java Backend Engineer k6 ${stamp}`,
    description: "Production Spring Boot, Kafka, PostgreSQL, OpenSearch and AWS platform work.",
    requirements: "Java 21, Spring Boot, Kafka, PostgreSQL, Docker",
    benefits: "Hybrid work, learning budget, healthy engineering culture",
    salaryMin: 3500,
    salaryMax: 6500,
    location: "Ho Chi Minh City",
    level: "Senior",
    type: "FULL_TIME",
    skills: ["Java", "Spring Boot", "Kafka", "AWS"],
  }, {
    flow: "fixture-job",
    sla: "gateway",
  });
  if (job.status !== 200) {
    return {};
  }
  const jobId = unwrap(job).id;
  request("PATCH", `/api/jobs/${jobId}/submit-review`, employerToken, null, {
    flow: "fixture-job-submit",
    sla: "gateway",
  });
  request("PATCH", `/api/admin/jobs/${jobId}/approve`, adminToken, null, {
    flow: "fixture-job-approve",
    sla: "gateway",
  });

  const application = request("POST", `/api/jobs/${jobId}/applications`, candidateToken, {
    cvUrl: "https://cdn.devhire.local/cv/k6-review.pdf",
    coverLetter: "Generated by role-based k6 smoke setup.",
  }, {
    flow: "fixture-application",
    sla: "gateway",
  });

  return {
    jobId,
    applicationId: application.status === 200 ? unwrap(application).id : null,
  };
}

export function setup() {
  const adminToken = login(__ENV.ADMIN_EMAIL || "admin@devhire.local", __ENV.ADMIN_PASSWORD || "Admin@123456");
  const employerToken = login(__ENV.EMPLOYER_EMAIL || "employer@devhire.local", __ENV.EMPLOYER_PASSWORD || "Employer@123456");
  const candidateToken = login(__ENV.CANDIDATE_EMAIL || "candidate@devhire.local", __ENV.CANDIDATE_PASSWORD || "Candidate@123456");

  const search = request("GET", "/api/jobs?keyword=Java&size=5", null, null, {
    flow: "setup-search",
    sla: "gateway",
  });
  const firstJob = search.status === 200 ? extractFirst(unwrap(search)) : null;
  const fixture = adminToken && employerToken && candidateToken
    ? createReviewFixture(adminToken, employerToken, candidateToken)
    : {};

  return {
    adminToken,
    employerToken,
    candidateToken,
    firstJobId: firstJob ? firstJob.id : fixture.jobId,
    reviewJobId: fixture.jobId || (firstJob ? firstJob.id : null),
    applicationId: fixture.applicationId,
  };
}

function publicSearch(data) {
  const response = request("GET", "/api/jobs?keyword=Java&skill=Kafka&size=10&sort=publishedAt,desc", null, null, {
    flow: "public-search",
    sla: "gateway",
  });
  check(response, {
    "public job search returns 200": (res) => res.status === 200,
    "public job search is json": (res) => (res.headers["Content-Type"] || "").includes("application/json"),
  });
  if (data.firstJobId) {
    const detail = request("GET", `/api/jobs/${data.firstJobId}`, null, null, {
      flow: "job-detail",
      sla: "gateway",
    });
    check(detail, {
      "public job detail returns 200": (res) => res.status === 200,
    });
  }
}

function candidateApply(data) {
  if (!data.firstJobId) {
    check(null, { "candidate apply has job fixture": () => false });
    return;
  }
  const token = registerCandidate(exec.scenario.iterationInTest);
  if (!token) {
    return;
  }
  const response = request("POST", `/api/jobs/${data.firstJobId}/applications`, token, {
    cvUrl: `https://cdn.devhire.local/cv/k6-${exec.vu.idInTest}-${exec.scenario.iterationInTest}.pdf`,
    coverLetter: "Role-based k6 candidate application smoke.",
  }, {
    flow: "candidate-apply",
    sla: "gateway",
  });
  check(response, {
    "candidate apply returns created application": (res) => res.status === 200 && Boolean(unwrap(res).id),
  });
}

function employerReview(data) {
  if (!data.reviewJobId || !data.employerToken) {
    check(null, { "employer review has fixture": () => false });
    return;
  }
  const list = request("GET", `/api/employer/jobs/${data.reviewJobId}/applications?page=0&size=10`, data.employerToken, null, {
    flow: "employer-review",
    sla: "gateway",
  });
  check(list, {
    "employer application list returns 200": (res) => res.status === 200,
  });
  if (data.applicationId) {
    const status = request("PATCH", `/api/applications/${data.applicationId}/status`, data.employerToken, {
      status: "INTERVIEW",
      note: "Role-based k6 employer review smoke.",
    }, {
      flow: "employer-status",
      sla: "gateway",
    });
    check(status, {
      "employer status update returns 200": (res) => res.status === 200,
    });
  }
}

function adminAudit(data) {
  if (!data.adminToken) {
    check(null, { "admin audit has token": () => false });
    return;
  }
  const response = request("GET", "/api/admin/audit-logs?page=0&size=20&sort=occurredAt,desc", data.adminToken, null, {
    flow: "admin-audit",
    sla: "gateway",
  });
  check(response, {
    "admin audit log returns 200": (res) => res.status === 200,
  });
}

function aiFallback(data) {
  if (!data.candidateToken) {
    check(null, { "ai fallback has token": () => false });
    return;
  }
  const response = request("POST", "/api/ai/chat", data.candidateToken, {
    message: "Explain this microservices platform to a recruiter with production risks and evidence.",
  }, {
    flow: "ai-assistant-fallback",
    sla: "ai",
  });
  check(response, {
    "ai assistant returns answer": (res) => res.status === 200 && Boolean(unwrap(res).answer),
    "ai assistant returns citations": (res) => {
      const body = res.status === 200 ? unwrap(res) : {};
      return Array.isArray(body.citations) && body.citations.length > 0;
    },
  });
}

const flows = {
  "public-search": publicSearch,
  "candidate-apply": candidateApply,
  "employer-review": employerReview,
  "admin-audit": adminAudit,
  "ai-fallback": aiFallback,
};

export default function (data) {
  if (scenario !== "all") {
    const flow = flows[scenario];
    if (!flow) {
      throw new Error(`Unknown scenario: ${scenario}`);
    }
    flow(data);
    sleep(1);
    return;
  }

  const all = [publicSearch, candidateApply, employerReview, adminAudit, aiFallback];
  all[exec.scenario.iterationInTest % all.length](data);
  sleep(1);
}
